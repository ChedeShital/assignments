package assignments;

public class Swaping {
	public static void main(String[] args) {
	    int i = 10;

	    int j = 7;
	    int temp;
	    temp = i;
	    i  = j;
	    j = temp;
	    
 

	    System.out.println(i);

	    System.out.println(j);

	    // Expected Result , i = 7  and j = 10  
	}

}
