package firstCodejava;

public class CyclicNature1 {
	public static void main(String[] args) {
	
		byte b1 = (byte)250;
		byte b2 = (byte)120;
		byte b3 = (byte)130;
		byte b4 = (byte)300;
		
		System.out.println(b1);
		System.out.println(b2);
		System.out.println(b3);
		System.out.println(b4);
		
	}
	
	
	
}
